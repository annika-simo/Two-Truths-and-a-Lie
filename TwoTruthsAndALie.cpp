#include <iostream>

/*
 *	This program outputs three statements: two truths 
 *	and one lie. If you look in the comments of this code
 *	you can see which is which.
 */

int main() {
	std::cout << "I have a black belt." << std::endl; //truth
	std::cout << "I've been to europe.\n"; //truth
	std::cout << "I'm fluent in Spanish and Vietnamese.\n"; //lie

	return 0;
}